						A prosa de Fernando Torres é um livre exercício de linguagem. 
						Frases curtas e impiedosas enfatizam o tom áspero das conversas 
						cotidianas e denunciam a mesquinhez de uma existência banal, 
						na qual grandeza e transcendência já não são mais possíveis. Não 
						a nós, porque depois de confrontada com o rotineiro, a busca pela 
						beleza já nos parece diluída, e a passagem do tempo se apresenta 
						como pura angústia; aflição nos ponteiros do relógio. Essa é a obra 
						de um escritor em formação que nos deixa claro o desejo de se aventurar 
						pelos ritmos do dia a dia e mergulhar sem medo nos domínios da escrita 
						automática, do delírio onírico, do fluxo de consciência e da livre transcrição 
						de estados mentais. Seus narradores são criaturas que tateiam às cegas, inseridas 
						em um território híbrido de sonho e realidade, sempre a balbuciar vestígios de seus 
						absurdos, desorientados, iludidos pela sordidez cotidiana, pela anestesia diária e 
						pela impossibilidade de se perseguir uma vida maior - daí o desejo de se libertar, 
						evadir, transgredir, atingir o êxtase pela linguagem e pelo amor, porque sim, há 
						espaço para o amor nessas linhas. Também o inconformismo se faz presente nesse livro, 
